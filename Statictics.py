from statistics import mean
from statistics import median
from statistics import mode
from statistics import variance

#mean
print(mean([1,3,4,8,2,7,9,4,8]))

#median
print(median([8,3,6,3,6,7,8,2,9,2,8,9]))
print(median([1,1,2,3,4,5,5,6,7,7]))

#mode
print(mode([1,1,2,3,4,5,5,6,7,7]))

#varience
print(variance([1,1,2,3,4,5,5,6,7,7]))


